let lightBulbStatus = "On";
//ให้เขียนโปรแกรมในการตรวจสอบสถานะของหลอดไฟด้วย If–Else Statement
//ถ้าสถานะของหลอดไฟ (lightBulbStatus) เป็น “On" ให้โปรแกรมหลอดไฟแสดงข้อความว่า 
// "Light bulb is On." ออกมาทางหน้าจอด้วย console.log()
//ให้โปรแกรมหลอดไฟแสดงข้อความว่า "Light bulb is Off." ออกมาทางหน้าจอด้วย console.log() 
// Start coding here.
if (lightBulbStatus === "On") {
    console.log("Light bulb is on");
} else if (lightBulbStatus === "Off") {
    console.log("Light bulb is Off.");
}

